#include "libft.h"

char		ft_is_uint(char *str)
{
	int		res;
	int		i;

	if (!str)
		return (0);
	res = 0;
	i = 0;
	(str[0] == '-' || str[0] == '+') ? str++ : 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		if ((res == 429496729 && str[i] >= '6') || res > 429496730)
			return (0);
		res = res * 10 + str[i++] - '0';
	}
	return ((i > 0 && str[i] == '\0') ? 1 : 0);
}
